'use client';

import * as React from 'react';
import { z } from 'zod';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
// Use native input elements instead of the shadcn `Input` component
import { Textarea } from '@/components/ui/textarea';
import { Button } from '@/components/ui/button';
import { Alert, AlertDescription } from '@/components/ui/alert';
import {
  Form,
  FormControl,
  FormDescription,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from '@/components/ui/form';
import { Loader2 } from 'lucide-react';
import { Card, CardContent } from '@/components/ui/card';
import { Checkbox } from '@/components/ui/checkbox';

export const contactSchema = z.object({
  name: z.string().trim().min(1, 'Bitte Name angeben'),
  email: z.string().trim().email('Bitte eine gültige E-Mail angeben'),
  company: z.string().trim().optional(),
  phone: z.string().trim().optional(),
  message: z.string().trim().optional(),
  consent: z.boolean().optional(),
});

export type ContactValues = z.infer<typeof contactSchema>;

type Props = {
  initialValues?: Partial<ContactValues>;
  onChange?: (values: Partial<ContactValues>) => void;
  submit?: boolean;
  action?: string;
  extra?: Record<string, unknown>;
  submitLabel?: string;
  successMessage?: string;
  layout?: 'plain' | 'card';
};

export function ContactForm({
  initialValues,
  onChange,
  submit = false,
  action = '/api/contact',
  extra,
  submitLabel = 'Senden',
  successMessage = 'Danke! Nachricht gesendet',
  layout = 'plain',
}: Props) {
  const form = useForm<ContactValues>({
    resolver: zodResolver(contactSchema),
    defaultValues: {
      name: initialValues?.name ?? '',
      email: initialValues?.email ?? '',
      company: initialValues?.company ?? '',
      phone: initialValues?.phone ?? '',
      message: initialValues?.message ?? '',
      consent: initialValues?.consent ?? false,
    },
    mode: submit ? 'onSubmit' : 'onChange',
  });

  React.useEffect(() => {
    if (!initialValues) return;
    form.reset({
      name: initialValues.name ?? form.getValues('name'),
      email: initialValues.email ?? form.getValues('email'),
      company: initialValues.company ?? form.getValues('company'),
      phone: initialValues.phone ?? form.getValues('phone'),
      message: initialValues.message ?? form.getValues('message'),
      consent: initialValues.consent ?? form.getValues('consent'),
    });
  }, [initialValues, form]);

  React.useEffect(() => {
    if (submit || !onChange) return;
    const sub = form.watch((values) => onChange(values));
    return () => sub.unsubscribe();
  }, [form, onChange, submit]);

  const [status, setStatus] = React.useState<'idle' | 'success' | 'error'>('idle');
  const [submitting, setSubmitting] = React.useState(false);
  const [errorMessage, setErrorMessage] = React.useState<string | null>(null);
  const consentId = React.useId();

  async function handleSubmit(values: ContactValues) {
    if (!submit) return;
    if (values.consent !== true) {
      form.setError('consent', { type: 'required', message: 'Bitte Zustimmung erteilen' });
      setStatus('idle');
      setErrorMessage(null);
      return;
    }
    try {
      setSubmitting(true);
      setStatus('idle');
      setErrorMessage(null);
      const res = await fetch(action, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          ...values,
          ...(extra || {}),
        }),
      });
      if (!res.ok) throw new Error('Request failed');
      setStatus('success');
      form.reset({
        name: '',
        email: '',
        company: '',
        phone: '',
        message: '',
        consent: false,
      });
    } catch (e: unknown) {
      console.error(e);
      setStatus('error');
      setErrorMessage(e instanceof Error ? e.message : null);
    } finally {
      setSubmitting(false);
    }
  }

  const gap = 'gap-4';
  const inputClasses =
    'w-full min-h-11 text-base rounded-none border-0 border-b border-neutral-800/70 bg-transparent px-0 py-2 text-foreground placeholder:text-muted-foreground focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary/30 focus-visible:ring-offset-2 focus-visible:ring-offset-transparent focus-visible:border-primary/80 touch-manipulation';
  const textareaClasses =
    'w-full min-h-28 text-base rounded-none border-0 border-b border-neutral-800/70 bg-transparent px-0 py-2 text-foreground placeholder:text-muted-foreground focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary/30 focus-visible:ring-offset-2 focus-visible:ring-offset-transparent focus-visible:border-primary/80 touch-manipulation';
  const submitHandler = submit ? form.handleSubmit(handleSubmit) : undefined;
  const handleTextareaKeyDown = (event: React.KeyboardEvent<HTMLTextAreaElement>) => {
    if (!submit) return;
    if ((event.metaKey || event.ctrlKey) && event.key === 'Enter') {
      event.preventDefault();
      submitHandler?.();
    }
  };
  const content = (
    <div className="w-full">
      <Form {...form}>
        <form
          noValidate
          onSubmit={submitHandler}
          className={`grid sm:grid-cols-2 ${gap}`}
        >
          <FormField
            control={form.control}
            name="name"
            render={({ field }) => (
              <FormItem>
                <FormLabel className="sr-only">Name</FormLabel>
                <FormControl>
                  <input
                    {...field}
                    placeholder="z. B. Maria Mustermann…"
                    autoComplete="name"
                    autoCapitalize="words"
                    className={inputClasses}
                    onBlur={(event) => {
                      const trimmed = event.target.value.trim();
                      field.onChange(trimmed);
                      field.onBlur();
                    }}
                  />
                </FormControl>
                <FormMessage />
              </FormItem>
            )}
          />
          <FormField
            control={form.control}
            name="email"
            render={({ field }) => (
              <FormItem>
                <FormLabel className="sr-only">E-Mail</FormLabel>
                <FormControl>
                  <input
                    type="email"
                    {...field}
                    placeholder="name@example.com…"
                    autoComplete="email"
                    inputMode="email"
                    autoCapitalize="none"
                    autoCorrect="off"
                    spellCheck={false}
                    className={inputClasses}
                    onBlur={(event) => {
                      const trimmed = event.target.value.trim();
                      field.onChange(trimmed);
                      field.onBlur();
                    }}
                  />
                </FormControl>
                <FormMessage />
              </FormItem>
            )}
          />
          <FormField
            control={form.control}
            name="company"
            render={({ field }) => (
              <FormItem className="sm:col-span-2">
                <FormLabel className="sr-only">Firma</FormLabel>
                <FormControl>
                  <input
                    {...field}
                    placeholder="z. B. Muster GmbH…"
                    autoComplete="organization"
                    className={inputClasses}
                    onBlur={(event) => {
                      const trimmed = event.target.value.trim();
                      field.onChange(trimmed);
                      field.onBlur();
                    }}
                  />
                </FormControl>
                <FormMessage />
              </FormItem>
            )}
          />
          <FormField
            control={form.control}
            name="phone"
            render={({ field }) => (
              <FormItem className="sm:col-span-2">
                <FormLabel>Telefon</FormLabel>
                <FormControl>
                  <input
                    type="tel"
                    {...field}
                    placeholder="z. B. +49 123 456789…"
                    autoComplete="tel"
                    inputMode="tel"
                    autoCorrect="off"
                    spellCheck={false}
                    className={inputClasses}
                    onBlur={(event) => {
                      const trimmed = event.target.value.trim();
                      field.onChange(trimmed);
                      field.onBlur();
                    }}
                  />
                </FormControl>
                <FormMessage />
              </FormItem>
            )}
          />
          <FormField
            control={form.control}
            name="message"
            render={({ field }) => (
              <FormItem className="sm:col-span-2">
                <FormLabel>Nachricht</FormLabel>
                <FormControl>
                  <Textarea
                    rows={3}
                    {...field}
                    placeholder="z. B. Anfrage zur Maschinenfreigabe…"
                    className={textareaClasses}
                    onBlur={(event) => {
                      const trimmed = event.target.value.trim();
                      field.onChange(trimmed);
                      field.onBlur();
                    }}
                    onKeyDown={handleTextareaKeyDown}
                  />
                </FormControl>
                <FormDescription>
                  Beschreiben Sie kurz Ihr Anliegen (optional, max. 500 Zeichen)
                </FormDescription>
                <FormMessage />
              </FormItem>
            )}
          />
          <FormField
            control={form.control}
            name="consent"
            render={({ field }) => (
              <FormItem className="sm:col-span-2">
                <div className="flex items-start gap-3">
                  <FormControl>
                    <Checkbox
                      id={consentId}
                      checked={field.value}
                      onCheckedChange={field.onChange}
                      name={field.name}
                      data-contact-consent
                      className="mt-0.5 touch-manipulation"
                    />
                  </FormControl>
                  <label htmlFor={consentId} className="flex-1 cursor-pointer">
                    <span className="block text-sm leading-5">
                      Ich stimme zu, dass meine Angaben zur Beantwortung meiner Anfrage verarbeitet
                      werden.
                    </span>
                    <span className="mt-1 block text-xs text-muted-foreground">
                      Ihre Daten werden gemäß DSGVO verarbeitet und nicht an Dritte weitergegeben.
                    </span>
                  </label>
                </div>
                <FormMessage />
              </FormItem>
            )}
          />

          {submit && (
            <div className="sm:col-span-2">
              <Button
                type="submit"
                disabled={submitting}
                aria-disabled={submitting}
                aria-busy={submitting}
                className="h-11 px-4 touch-manipulation"
              >
                {submitting && <Loader2 className="h-4 w-4 animate-spin" aria-hidden="true" />}
                {submitLabel}
              </Button>
            </div>
          )}
        </form>
        {submit && status === 'success' && (
          <Alert
            className="mt-4 animate-fade-in"
            variant="default"
            role="status"
            aria-live="polite"
          >
            <AlertDescription>{successMessage}</AlertDescription>
          </Alert>
        )}
        {submit && status === 'error' && (
          <Alert
            className="mt-4 animate-fade-in"
            variant="destructive"
            role="alert"
            aria-live="assertive"
          >
            <AlertDescription>
              {errorMessage ?? 'Etwas ist schiefgelaufen. Versuch es erneut.'}
            </AlertDescription>
          </Alert>
        )}
      </Form>
    </div>
  );

  if (layout === 'card') {
    return (
      <Card className="rounded-2xl border-0 shadow-none bg-transparent">
        <CardContent>{content}</CardContent>
      </Card>
    );
  }
  return content;
}

export default ContactForm;
